package edu.raniel.tiposvariaveis;

public class TiposVariaveis {
    // Tipo inteiro (int)
    int numero = 30;

    // Tipo ponto flutuante (double)
    double altura = 1.75;

    //ponto flutuante do tipo float
    float peso = 64.5f;

    //Tipo de numero longo
    long numeroLongo = 879845645123168784l;

    // Tipo caractere (char)
    char letra = 'A';

    // Tipo booleano (boolean)
    boolean valor = false;

    // Tipo String (sequência de caracteres)
    String nome = "Raniel";

    //Tipo final(constante, ou seja, o valor não pode ser alterado)
    final double VALOR_DE_PI = 3.14;

}
